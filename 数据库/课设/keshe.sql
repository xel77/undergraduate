    DECLARE v_address VARCHAR(255);
    DECLARE v_shelf_id INT;
    DECLARE done INT DEFAULT 0;
    DECLARE cur_num INT;
    DECLARE cur_out_time DATETIME;

    -- 声明游标
    DECLARE cur_order CURSOR FOR
        SELECT product_id, num
        FROM out_items
        WHERE order_id = p_order_id;

    DECLARE cur CURSOR FOR
        SELECT address, num, out_time
        FROM in_items
        WHERE product_id = v_product_id
        ORDER BY out_time ASC;  -- 按保质期升序排序

    -- 声明游标结束处理程序
    DECLARE CONTINUE HANDLER FOR NOT FOUND SET done = 1;

    -- 打开订单游标
    OPEN cur_order;

    -- 遍历订单中的商品信息
    order_loop: LOOP
        FETCH cur_order INTO v_product_id, v_quantity;

        -- 处理游标结束
        IF done THEN
            LEAVE order_loop;
        END IF;

        -- 更新商品总库存
        UPDATE product
        SET product_num = product_num - v_quantity
        WHERE product_id = v_product_id;

        -- 打开 in_items 游标
        SET done = 0;  -- 重置游标结束标志
        OPEN cur;

        -- 处理 in_items 表中的商品
        address_loop: LOOP
            FETCH cur INTO v_address, cur_num, cur_out_time;

            IF done THEN
                LEAVE address_loop;
            END IF;

            -- 如果当前地址的商品数量足够满足需求
            IF cur_num >= v_quantity THEN
                -- 更新当前批次的商品数量
                UPDATE in_items
                SET num = num - v_quantity
                WHERE product_id = v_product_id
                AND address = v_address;

                -- 提取货架ID并更新货架容量
                SET v_shelf_id = CAST(SUBSTRING_INDEX(v_address, ',', 1) AS UNSIGNED);

                UPDATE hj
                SET hj_current_size = hj_current_size + v_quantity
                WHERE hj_id = v_shelf_id;

                SET v_quantity = 0;  -- 商品数量已满足要求，退出循环
                LEAVE address_loop;
            ELSE
                -- 如果当前地址的商品数量不够
                UPDATE in_items
                SET num = 0
                WHERE product_id = v_product_id
                AND address = v_address;

                -- 减少商品数量
                SET v_quantity = v_quantity - cur_num;

                -- 提取货架ID并更新货架容量
                SET v_shelf_id = CAST(SUBSTRING_INDEX(v_address, ',', 1) AS UNSIGNED);

                UPDATE hj
                SET hj_current_size = hj_current_size + cur_num
                WHERE hj_id = v_shelf_id;
            END IF;
        END LOOP;

        CLOSE cur;
    END LOOP;

    CLOSE cur_order;

 

END
;;
delimiter ;

