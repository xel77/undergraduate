delimiter ;;
CREATE DEFINER=`root`@`%` PROCEDURE `update_transaction_counts`()
BEGIN
    -- 更新未处理的入库订单数量
    UPDATE transaction
    SET non_in_store_num = in_store_total_num - in_store_num
    WHERE in_store_total_num > in_store_num;

    -- 更新未处理的出库订单数量
    UPDATE transaction
    SET non_out_store_num = out_store_total_num - out_store_num
    WHERE out_store_total_num > out_store_num;
END
;;
delimiter ;

