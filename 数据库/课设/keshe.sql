DROP PROCEDURE IF EXISTS `GetProductLocation`;
delimiter ;;
CREATE DEFINER=`root`@`%` PROCEDURE `GetProductLocation`(
    IN p_product_id INT,  -- 输入的商品ID
    IN p_quantity INT     -- 输入的要取出的商品数量
)
BEGIN
    -- 声明变量
    DECLARE remaining_quantity INT DEFAULT p_quantity;  -- 剩余要取出的数量
    DECLARE cur_address VARCHAR(255);  -- 存储当前地址
    DECLARE cur_num INT;  -- 存储当前批次可用的数量
    DECLARE total_stock INT;  -- 总库存
    DECLARE min_stock INT;  -- 最低库存
    DECLARE available_quantity INT;  -- 可用库存（总库存 - 最低库存）
    DECLARE not_fetched INT DEFAULT 0;  -- 未取到的数量
    DECLARE done INT DEFAULT 0;  -- 游标结束标志
    DECLARE order_status INT;  -- 订单状态
    
    -- 声明游标
    DECLARE cur CURSOR FOR
    SELECT address, num
    FROM in_items
    WHERE product_id = p_product_id
    AND address IS NOT NULL  -- 仅选择有地址的记录
    ORDER BY (out_time - NOW()), out_time ASC;  -- 按保质期和入库时间排序

    -- 声明游标处理结束的处理程序
    DECLARE CONTINUE HANDLER FOR NOT FOUND SET done = 1;

    -- 查询商品的总库存、最低库存和订单状态
    SELECT product_num, product_lownum 
    INTO total_stock, min_stock
    FROM product
    WHERE product_id = p_product_id;

    -- 计算可用库存（总库存 - 最低库存）
    SET available_quantity = total_stock - min_stock;

    -- 检查可用库存是否足够
    IF available_quantity >= p_quantity THEN
        -- 可用库存足够，正常提货
        SET remaining_quantity = p_quantity;
    ELSE
        -- 可用库存不足，只能提到最低库存
        SET remaining_quantity = available_quantity;
        SET not_fetched = p_quantity - remaining_quantity;  -- 记录未取到的数量
    END IF;

    -- 创建临时表用于存储查询结果
    CREATE TEMPORARY TABLE TempProductLocation (
        location VARCHAR(255), 
        fetched_quantity INT
    );

    -- 打开游标
    OPEN cur;

    -- 循环遍历游标数据
    read_loop: LOOP
        FETCH cur INTO cur_address, cur_num;

        -- 如果已经取够了数量，或者游标读取结束，退出循环
        IF done = 1 OR remaining_quantity <= 0 THEN
            LEAVE read_loop;
        END IF;

        -- 如果当前批次可提供的数量足够满足剩余需求
        IF cur_num >= remaining_quantity THEN
            -- 插入到临时表，表示可以从该地址提取 remaining_quantity 的数量
            INSERT INTO TempProductLocation (location, fetched_quantity)
            VALUES (cur_address, remaining_quantity);

            -- 取货完成，剩余数量为0
            SET remaining_quantity = 0;
        ELSE
            -- 如果当前批次数量不够，则插入当前批次的地址和数量
            INSERT INTO TempProductLocation (location, fetched_quantity)
            VALUES (cur_address, cur_num);

            -- 更新剩余需要提取的数量
            SET remaining_quantity = remaining_quantity - cur_num;
        END IF;
    END LOOP;

    -- 关闭游标
    CLOSE cur;

    -- 查询结果，返回可以提取的地址和数量
    IF (SELECT COUNT(*) FROM TempProductLocation) > 0 THEN
        SELECT location, fetched_quantity 
        FROM TempProductLocation;
    ELSE
        SELECT '没有可用库存' AS warning_message;
    END IF;

    -- 如果有未取到的数量，返回提示信息
    IF not_fetched > 0 THEN
        SELECT CONCAT('还有 ', not_fetched, ' 件商品未取到，库存不足') AS warning_message;
    END IF;

    -- 删除临时表
    DROP TEMPORARY TABLE TempProductLocation;

END
;;
delimiter ;

