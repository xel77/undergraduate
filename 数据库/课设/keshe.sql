delimiter ;;
CREATE DEFINER=`root`@`%` PROCEDURE `find_available_shelves`(
    IN p_order_id INT  -- 订单ID
)
BEGIN
    -- 声明变量
    DECLARE total_product_qty INT DEFAULT 0;  -- 订单中所有商品的总数量
    DECLARE remaining_qty INT DEFAULT 0;  -- 剩余的商品数量
    DECLARE shelf_id INT DEFAULT NULL;  -- 当前货架ID
    DECLARE shelf_space INT DEFAULT 0;  -- 当前货架可用空间
    DECLARE done INT DEFAULT 0;  -- 控制游标结束
    DECLARE selected_shelf_id INT DEFAULT NULL;  -- 选中的货架ID

    -- 声明游标，获取货架的可用空间，按剩余空间从大到小排序
    DECLARE cur_shelves CURSOR FOR
        SELECT hj_id, hj_current_size
        FROM hj
        WHERE hj_current_size > 0
        ORDER BY hj_current_size DESC;

    -- 声明处理游标结束的处理程序
    DECLARE CONTINUE HANDLER FOR NOT FOUND SET done = 1;

    -- 第1步：查询给定订单ID在明细表in_items中所有商品的总数量
    SELECT SUM(num) INTO total_product_qty
    FROM in_items
    WHERE order_id = p_order_id;

    -- 初始化剩余商品数量
    SET remaining_qty = total_product_qty;

    -- 打开货架游标，开始遍历货架
    OPEN cur_shelves;

    -- 遍历货架，找到合适的货架
    read_loop: LOOP
        -- 获取当前货架的ID和可用空间
        FETCH cur_shelves INTO shelf_id, shelf_space;

        -- 如果游标读取结束，退出循环
        IF done = 1 THEN
            LEAVE read_loop;
        END IF;

        -- 如果剩余商品数量小于等于当前货架空间，直接放入
        IF remaining_qty <= shelf_space THEN
            -- 更新货架容量
            UPDATE hj
            SET hj_current_size = hj_current_size - remaining_qty
            WHERE hj_id = shelf_id;

            -- 商品数量完全匹配，记录货架ID并退出循环
            SET remaining_qty = 0;
            SET selected_shelf_id = shelf_id;
            LEAVE read_loop;
        ELSE
            -- 当前货架只能容纳部分商品，继续寻找下一个货架
            UPDATE hj
            SET hj_current_size = 0
            WHERE hj_id = shelf_id;

            -- 更新剩余商品数量
            SET remaining_qty = remaining_qty - shelf_space;
        END IF;
    END LOOP;

    -- 关闭货架游标
    CLOSE cur_shelves;

    -- 返回结果
    IF remaining_qty = 0 THEN
        -- 如果剩余数量为0，返回找到的货架ID
        SELECT selected_shelf_id AS available_shelf;
    ELSE
        -- 否则返回提示信息：没有足够的空间放下商品
        SELECT '没有足够的空间放下这批货' AS message;
    END IF;

END
;;
delimiter ;

