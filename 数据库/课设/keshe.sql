CREATE DEFINER=`root`@`%` PROCEDURE `CheckProductStock`()
BEGIN
    DECLARE done INT DEFAULT FALSE;
    DECLARE v_product_id INT;
    DECLARE v_product_num INT;
    DECLARE v_product_lownum INT;

    -- 创建游标来遍历所有产品
    DECLARE product_cursor CURSOR FOR SELECT product_id, product_num, product_lownum FROM product;
    DECLARE CONTINUE HANDLER FOR NOT FOUND SET done = TRUE;

    OPEN product_cursor;

    read_loop: LOOP
        FETCH product_cursor INTO v_product_id, v_product_num, v_product_lownum;
        IF done THEN
            LEAVE read_loop;
        END IF;

        -- 检查库存是否小于或等于最低库存
        IF v_product_num <= v_product_lownum THEN
            -- 抛出一个警告，提示库存达到最低限额
            SIGNAL SQLSTATE '45000'
            SET MESSAGE_TEXT = 'Warning: Stock  has reached or fallen below the minimum limit.';
        END IF;
    END LOOP;

    CLOSE product_cursor;
END
;;
delimiter ;

