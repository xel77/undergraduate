-- ----------------------------
-- Procedure structure for UpdateLoginPassword
-- ----------------------------
DROP PROCEDURE IF EXISTS `UpdateLoginPassword`;
delimiter ;;
CREATE DEFINER=`root`@`%` PROCEDURE `UpdateLoginPassword`(
    IN p_yg_id INT,                -- 员工ID
    IN p_old_password VARCHAR(255), -- 旧密码
    IN p_new_password VARCHAR(255)  -- 新密码
)
BEGIN
    DECLARE v_existing_password VARCHAR(255); -- 用于存储数据库中的当前密码
    DECLARE v_password_matched BOOLEAN DEFAULT FALSE; -- 用于检查密码是否匹配

    -- 查询员工的现有密码
    SELECT yg_passwd INTO v_existing_password
    FROM login
    WHERE yg_id = p_yg_id;

    -- 检查旧密码是否匹配
    IF v_existing_password = p_old_password THEN
        SET v_password_matched = TRUE;
    ELSE
        SET v_password_matched = FALSE;
    END IF;

    -- 如果旧密码匹配，则更新为新密码
    IF v_password_matched THEN
        UPDATE login
        SET yg_passwd = p_new_password
        WHERE yg_id = p_yg_id;

        -- 提示成功
        SELECT '修改成功' AS message;
    ELSE
        -- 如果旧密码不匹配，返回错误信息
        SELECT '密码不对' AS message;
    END IF;
END
;;
delimiter ;

