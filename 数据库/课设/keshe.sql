CREATE DEFINER=`root`@`%` PROCEDURE `handle_in_order`(
    IN p_handle_yg_id INT,            -- 员工ID
    IN p_order_id INT,                -- 订单ID
    IN p_items_info JSON              -- 商品ID和存放地址的JSON数组，例如：'[{"product_id": 1, "address": "1,1", "quantity": 10}, {"product_id": 2, "address": "2,3", "quantity": 5}]'
)
BEGIN
    DECLARE order_prefix CHAR(1);
    DECLARE i INT DEFAULT 0;
    DECLARE items_count INT;
    DECLARE v_product_id INT;
    DECLARE v_address VARCHAR(100);
    DECLARE v_quantity INT;
    DECLARE v_shelf_id INT;
    DECLARE v_shelf_space INT;
    DECLARE current_status INT;

    -- 检查订单ID的开头数字
    SET order_prefix = LEFT(p_order_id, 1);

    IF order_prefix = '1' THEN
        -- 检查订单是否已被处理
        SELECT order_status
        INTO current_status
        FROM in_store
        WHERE order_id = p_order_id;

        -- 如果订单已经被处理，则返回相应的消息
        IF current_status = 1 THEN
            SELECT '入库订单已被处理，无需重复处理' AS result_message;
        ELSE
            -- 处理入库订单
            -- Step 1: 更新入库表，设置处理员工ID，修改订单状态为已处理
            UPDATE in_store
            SET handle_yg_id = p_handle_yg_id,
                order_status = 1,          -- 设置为已处理状态
                handle_time = NOW()        -- 设置处理时间为当前时间
            WHERE order_id = p_order_id;

            -- 获取 JSON 数组的长度
            SET items_count = JSON_LENGTH(p_items_info);

            -- 循环处理 JSON 数组中的每个商品
            WHILE i < items_count DO
                -- 提取每个商品的信息
                SET v_product_id = JSON_UNQUOTE(JSON_EXTRACT(p_items_info, CONCAT('$[', i, '].product_id')));
                SET v_address = JSON_UNQUOTE(JSON_EXTRACT(p_items_info, CONCAT('$[', i, '].address')));
                SET v_quantity = JSON_UNQUOTE(JSON_EXTRACT(p_items_info, CONCAT('$[', i, '].quantity')));

                -- 从地址中提取货架ID（地址格式为 '货架ID,其他信息'，使用 SUBSTRING_INDEX 提取第一个数字）
                SET v_shelf_id = SUBSTRING_INDEX(v_address, ',', 1);

                -- 获取货架的当前容量
                SELECT hj_current_size INTO v_shelf_space
                FROM hj
                WHERE hj_id = v_shelf_id;

                -- 更新货架容量，将当前容量加上商品数量（入库时增加容量）
                UPDATE hj
                SET hj_current_size = v_shelf_space + v_quantity
                WHERE hj_id = v_shelf_id;
                
                -- 只更新地址，不插入新记录
                UPDATE in_items
                SET address = v_address
                WHERE product_id = v_product_id AND order_id = p_order_id;

                -- 更新商品表中的库存数量
                UPDATE product
                SET product_num = product_num + v_quantity
                WHERE product_id = v_product_id;

                -- 处理下一个商品
                SET i = i + 1;
            END WHILE;

            -- 返回处理成功的信息
            SELECT CONCAT('入库订单 ', p_order_id, ' 已被处理') AS result_message;

            -- 插入日志信息
            INSERT INTO log_table (log_message) 
            VALUES (CONCAT('入库单 ', p_order_id, ' 已由员工 ', p_handle_yg_id, ' 处理'));

        END IF;

    ELSE
        -- 如果订单ID的开头数字不是1，返回错误信息
        SELECT '无效的订单ID前缀' AS result_message;
    END IF;

END
;;
delimiter ;

