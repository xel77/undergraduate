CREATE DEFINER=`root`@`%` PROCEDURE `ManageOutItems`(
    IN p_action_type INT,       -- 操作类型: 0 = 添加, 1 = 修改, 2 = 查询, 3 = 追加
    IN p_order_id INT,          -- 订单ID
    IN p_items JSON             -- 商品明细，以JSON格式输入（仅在添加、修改和追加时需要）
)
BEGIN
    DECLARE v_product_id INT;   -- 商品ID
    DECLARE v_num INT;          -- 商品数量
    DECLARE done INT DEFAULT 0; -- 游标完成标志

    -- 声明游标以遍历JSON数组
    DECLARE item_cursor CURSOR FOR
        SELECT JSON_UNQUOTE(JSON_EXTRACT(item.value, '$.product_id')) AS product_id,
               JSON_UNQUOTE(JSON_EXTRACT(item.value, '$.num')) AS num
        FROM JSON_TABLE(p_items, '$[*]' COLUMNS (value JSON PATH '$')) AS item;

    -- 处理游标完成情况
    DECLARE CONTINUE HANDLER FOR NOT FOUND SET done = 1;

    -- 添加出库明细
    IF p_action_type = 0 THEN
        -- 插入出库明细
        INSERT INTO out_items (order_id, product_id, num)
        SELECT p_order_id,
               JSON_UNQUOTE(JSON_EXTRACT(item.value, '$.product_id')) AS product_id,
               JSON_UNQUOTE(JSON_EXTRACT(item.value, '$.num')) AS num
        FROM JSON_TABLE(p_items, '$[*]' COLUMNS (value JSON PATH '$')) AS item;

    -- 修改出库明细
    ELSEIF p_action_type = 1 THEN
        -- 删除并重新插入出库明细
        DELETE FROM out_items WHERE order_id = p_order_id;

        -- 插入新的出库明细
        INSERT INTO out_items (order_id, product_id, num)
        SELECT p_order_id,
               JSON_UNQUOTE(JSON_EXTRACT(item.value, '$.product_id')) AS product_id,
               JSON_UNQUOTE(JSON_EXTRACT(item.value, '$.num')) AS num
        FROM JSON_TABLE(p_items, '$[*]' COLUMNS (value JSON PATH '$')) AS item;

    -- 查询出库明细
    ELSEIF p_action_type = 2 THEN
        -- 查询出库明细
        SELECT * FROM out_items WHERE order_id = p_order_id;

    -- 追加出库明细
    ELSEIF p_action_type = 3 THEN
        -- 打开游标
        OPEN item_cursor;

        item_loop: LOOP
            -- 从游标中取出数据
            FETCH item_cursor INTO v_product_id, v_num;
            IF done = 1 THEN
                LEAVE item_loop;
            END IF;

            -- 检查是否存在相同的商品编号
            IF EXISTS (SELECT 1 FROM out_items WHERE order_id = p_order_id AND product_id = v_product_id) THEN
                -- 更新数量，将新数量加到现有的数量上
                UPDATE out_items
                SET num = num + v_num
                WHERE order_id = p_order_id AND product_id = v_product_id;
            ELSE
                -- 如果不存在，则插入新记录
                INSERT INTO out_items (order_id, product_id, num)
                VALUES (p_order_id, v_product_id, v_num);
            END IF;
        END LOOP;

        -- 关闭游标
        CLOSE item_cursor;
    END IF;
END
